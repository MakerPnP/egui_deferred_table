use std::fmt::Display;

#[derive(Debug)]
pub enum Kind {
    Human,
    Alien,
    Mutant,
    Robot,
    Animal,
}

impl Display for Kind {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(f, "{}", format!("{:?}", self).to_lowercase())
    }
}


